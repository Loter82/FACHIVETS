/**
 * Класифікація типів документів Unipro (uniDocuments.fDocType).
 *
 * Значення взяті за стандартом Unipro і підтверджені реальними даними
 * на джерелі Fachivets (станом на 2026-06):
 *   1  — Прихід / Накладна постачальника
 *   2  — Видаткова накладна (опт-продаж)
 *   3  — Списання
 *   4  — Чек (роздрібний продаж РРО)
 *   5  — Переміщення між складами
 *   6  — Повернення постачальнику
 *   7  — Внутрішній (ревізія / переоблік)
 *   11 — Повернення від клієнта
 *   14 — Сервісний
 *   15 — Інше
 *
 * У майбутньому план — синхронізувати uniDocTypes і брати назву звідти.
 */

export const DOC_TYPE_LABELS_UK: Record<number, string> = {
  1: 'Прихід',
  2: 'Видаткова накладна',
  3: 'Списання',
  4: 'Чек',
  5: 'Переміщення',
  6: 'Повернення постачальнику',
  7: 'Внутрішній',
  11: 'Повернення від клієнта',
  14: 'Сервісний',
  15: 'Інше',
};

/** Документ роздрібного продажу (чек з РРО, оформлюється через Екран касира). */
export const RETAIL_DOC_TYPE = 4 as const;

/** Документ оптового продажу (видаткова накладна, оформлюється в бек-офісі). */
export const WHOLESALE_DOC_TYPE = 2 as const;

/** Документи, які формують виторг (від клієнта). Чек + Видаткова накладна. */
export const SALE_DOC_TYPES: readonly number[] = [WHOLESALE_DOC_TYPE, RETAIL_DOC_TYPE] as const;

/** Документи повернень від клієнта (зменшують виторг). */
export const RETURN_DOC_TYPES: readonly number[] = [11] as const;

export function docTypeLabel(docType: number): string {
  return DOC_TYPE_LABELS_UK[docType] ?? `Тип ${docType}`;
}
